<h2>about</h2>


<p><strong>Have you ever wanted to absolutely ensure you will never enter the Kingdom of Heaven?</strong></p>

<p>This seems like a straightforward task. Surely you just have to do something sufficiently evil? Yes, this might be hard work and time-consuming, but you would think it would get the job done, right?</p>


<p>The problem is, Jesus (and by extension the New Testament Abrahamic God) are <em>very</em> forgiving. In many modern Christian traditions, no matter what horrifying atrocities you commit, you can come to Christ and ask for forgiveness and Saint Robert's your uncle, you are good to go.</p>

<p>Enter Matthew 12. </p>